#!/usr/bin/env python3
"""
 function concat that takes a string str1 and a string str2 as arguments and returns a concatenated string function concat that takes a string str1 and a string str2 as arguments and returns a concatenated string
"""


def concat(a: str, b: str) -> str:
    """
    Concatenates two strings
    Args:
        a (str)
        b (str)
    Return:
        (str)
    """
    return a + b